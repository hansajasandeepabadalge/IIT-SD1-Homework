num1 = int(input("Enter your First Number: "))

add = 10
add+=num1
print(f"If you add {num1} to {add}", add)

sub = 10
sub-=num1
print(f"If you add {num1} to {add}", sub)

mul = 10
mul*=num1
print(f"If you add {num1} to {add}", mul)

div = 10
div/=num1
print(f"If you add {num1} to {add}", div)

mod = 10
mod%=num1
print(f"If you add {num1} to {add}", mod)

flod = 10
flod/=num1
print(f"If you add {num1} to {add}", flod)

expo = 10
expo**=num1
print(f"If you add {num1} to {add}", expo)

